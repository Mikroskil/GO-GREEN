<h2>Suku Batak</h2>

<h3>Orang Batak adalah penutur bahasa Austronesia  namun tidak diketahui kapan nenek moyang orang Batak pertama kali bermukim di Tapanuli dan Sumatera Timur. Bahasa dan bukti-bukti arkeologi menunjukkan bahwa orang yang berbahasa Austronesia dari Taiwan telah berpindah ke wilayah Filipina dan Indonesia sekitar 2.500 tahun lalu, yaitu di zaman batu muda (Neolitikum). Karena hingga sekarang belum ada artefak Neolitikum (Zaman Batu Muda) yang ditemukan di wilayah Batak maka dapat diduga bahwa nenek moyang Batak baru bermigrasi ke Sumatera Utara di zaman logam. Pada abad ke-6, pedagang-pedagang Tamil asal India mendirikan kota dagang Barus, di pesisir barat Sumatera Utara. Mereka berdagang kapur Barus yang diusahakan oleh petani-petani di pedalaman. Kapur Barus dari tanah Batak bermutu tinggi sehingga menjadi salah satu komoditas ekspor di samping kemenyan. Pada abad ke-10, Barus diserang oleh Sriwijaya. Hal ini menyebabkan terusirnya pedagang-pedagang Tamil dari pesisir Sumatera. Pada masa-masa berikutnya, perdagangan kapur Barus mulai banyak dikuasai oleh pedagang Minangkabau yang mendirikan koloni di pesisir barat dan timur Sumatera Utara. Koloni-koloni mereka terbentang dari Barus, Sorkam, hingga Natal.</h3>

<h2>Identitas Batak</h2>

<h3>R.W Liddle mengatakan, bahwa sebelum abad ke-20 di Sumatera bagian utara tidak terdapat kelompok etnis sebagai satuan sosial yang koheren. Menurutnya sampai abad ke-19, interaksi sosial di daerah itu hanya terbatas pada hubungan antar individu, antar kelompok kekerabatan, atau antar kampung. Dan hampir tidak ada kesadaran untuk menjadi bagian dari satuan-satuan sosial dan politik yang lebih besar. Pendapat lain mengemukakan, bahwa munculnya kesadaran mengenai sebuah keluarga besar Batak baru terjadi pada zaman kolonial. Dalam disertasinya J. Pardede mengemukakan bahwa istilah "Tanah Batak" dan "rakyat Batak" diciptakan oleh pihak asing. Sebaliknya, Siti Omas Manurung, seorang istri dari putra pendeta Batak Toba menyatakan, bahwa sebelum kedatangan Belanda, semua orang baik karo maupun Simalungun mengakui dirinya sebagai Batak, dan Belandalah yang telah membuat terpisahnya kelompok-kelompok tersebut. Sebuah mitos yang memiliki berbagai macam versi menyatakan, bahwa Pusuk Buhit, salah satu puncak di barat Danau Toba, adalah tempat "kelahiran" bangsa Batak. Selain itu mitos-mitos tersebut juga menyatakan bahwa nenek moyang orang Batak berasal dari Samosir.

Terbentuknya masyarakat Batak yang tersusun dari berbagai macam marga, sebagian disebabkan karena adanya migrasi keluarga-keluarga dari wilayah lain di Sumatra. Penelitian penting tentang tradisi Karo dilakukan oleh J.H Neumann, berdasarkan sastra lisan dan transkripsi dua naskah setempat, yaitu Pustaka Kembaren dan Pustaka Ginting. Menurut Pustaka Kembaren, daerah asal marga Kembaren dari Pagaruyung di Minangkabau. Orang Tamil diperkirakan juga menjadi unsur pembentuk masyarakat Karo. Hal ini terlihat dari banyaknya nama marga Karo yang diturunkan dari Bahasa Tamil. Orang-orang Tamil yang menjadi pedagang di pantai barat, lari ke pedalaman Sumatera akibat serangan pasukan Minangkabau yang datang pada abad ke-14 untuk menguasai Barus.</h3>
		       
